<h1>Advice on Selecting Volunteer Work</h1>

<p>This site was built to make finding volunteer opportunities as simple as possible. Below, you can see resources for searching charities near you. You can also select charity events on your friends pages and add them to your calendar easily.</p>
<br>
<p>Oftentimes, you will have to find a volunteer event yourself. Conventional advice says to look for groups in your community or people who have dealt with similar problems.</p>
<br>
<p>At 10 Charity Challenge, we actually suggest the inverse. We suggest looking for people of different colors and creeds, statuses and situations. One of the opportunities volunteer work affords us is the chance to learn more about people different from ourselves. When considering a place to start, look outside your bubble!</p>
